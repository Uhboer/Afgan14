[UsedImplicitly]
public sealed class TransmutationCircleBoundUserInterface : BoundUserInterface
{
    [Dependency] private readonly IClyde _displayManager = default!;
    [Dependency] private readonly IInputManager _inputManager = default!;

    private TransmutationCircleMenu? _menu;

    public TransmutationCircleBoundUserInterface(EntityUid owner, Enum uiKey) : base(owner, uiKey)
    {
        IoCManager.InjectDependencies(this);
    }

    protected override void Open()
    {
        base.Open();

        _menu = new TransmutationCircleMenu(this);
        _menu.OnClose += Close;

        // Открываем по центру мыши как RCD
        var vpSize = _displayManager.ScreenSize;
        _menu.OpenCenteredAt(_inputManager.MouseScreenPosition.Position / vpSize);
    }

    protected override void UpdateState(BoundUserInterfaceState state)
    {
        if (state is TransmutationCircleState s)
            _menu?.UpdateState(s);
    }

    public void SendCraft(string recipeId)
    {
        SendMessage(new TransmutationCircleSelectMessage(recipeId));
    }

    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);
        if (!disposing) return;
        _menu?.Dispose();
    }
}

[GenerateTypedNameReferences]
public sealed partial class TransmutationCircleMenu : RadialMenu
{
    [Dependency] private readonly IEntityManager _entMan = default!;
    [Dependency] private readonly IPrototypeManager _proto = default!;

    private readonly SpriteSystem _sprite;
    private readonly TransmutationCircleBoundUserInterface _bui;

    public TransmutationCircleMenu(TransmutationCircleBoundUserInterface bui)
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        _sprite = _entMan.System<SpriteSystem>();
        _bui = bui;
    }

    public void UpdateState(TransmutationCircleState state)
    {
        var main = FindControl<RadialContainer>("Main");
        if (main == null)
            return;

        main.DisposeAllChildren();

        foreach (var recipeId in state.AvailableRecipes)
        {
            if (!_proto.TryIndex<TransmutationRecipeSimple>(recipeId, out var recipe))
                continue;

            // Название результата для тултипа
            string resultName = recipe.Result;
            if (_proto.TryIndex<EntityPrototype>(recipe.Result, out var entityProto))
                resultName = entityProto.Name;

            // Материалы для тултипа
            var matParts = new List<string>();
            foreach (var (tag, count) in recipe.Materials)
                matParts.Add(count > 1 ? $"{tag} x{count}" : tag);
            var matString = string.Join(", ", matParts);

            var btn = new TransmutationButton
            {
                StyleClasses = { "RadialMenuButton" },
                SetSize = new Vector2(64f, 64f),
                ToolTip = $"{resultName}\nНужно: {matString}",
                RecipeId = recipeId,
            };

            // Иконка спрайта результата
            if (_proto.TryIndex<EntityPrototype>(recipe.Result, out var ep) && ep.Components.ContainsKey("Sprite"))
            {
                var tex = new TextureRect
                {
                    VerticalAlignment = VAlignment.Center,
                    HorizontalAlignment = HAlignment.Center,
                    TextureScale = new Vector2(2f, 2f),
                    Stretch = TextureRect.StretchMode.KeepAspectCentered,
                };

                // Берём иконку через SpriteSystem
                var specifier = new SpriteSpecifier.EntityPrototype(recipe.Result);
                tex.Texture = _sprite.Frame0(specifier);
                btn.AddChild(tex);
            }

            btn.OnButtonUp += _ =>
            {
                _bui.SendCraft(btn.RecipeId);
                Close();
            };

            main.AddChild(btn);
        }

        // Если нечего крафтить — кнопки не добавляем, меню само закроется через ContextualButton
    }
}

public sealed class TransmutationButton : TextureButton
{
    public string RecipeId { get; set; } = string.Empty;
}
